import { copyFile, mkdir, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = resolve(__dirname, '..');
const vendorDir = resolve(rootDir, 'vendor');

const assets = [
  ['node_modules/maplibre-gl/dist/maplibre-gl.js', 'maplibre-gl.js'],
  ['node_modules/maplibre-gl/dist/maplibre-gl.css', 'maplibre-gl.css'],
  ['node_modules/maplibre-gl/LICENSE.txt', 'maplibre-LICENSE.txt'],
  ['node_modules/pmtiles/dist/pmtiles.js', 'pmtiles.js'],
];

await mkdir(vendorDir, { recursive: true });
await Promise.all(
  assets.map(([source, destination]) =>
    copyFile(resolve(rootDir, source), resolve(vendorDir, destination)),
  ),
);
const [basemapStyle, basemapTileJson] = await Promise.all(
  [
    'https://tiles.openfreemap.org/styles/liberty',
    'https://tiles.openfreemap.org/planet',
  ].map(async (url) => {
    const response = await fetch(url);
    if (!response.ok) throw new Error(`Failed to fetch ${url}: ${response.status}`);
    return response.json();
  }),
);
basemapStyle.sources.openmaptiles = {
  type: 'vector',
  tiles: basemapTileJson.tiles,
  minzoom: basemapTileJson.minzoom ?? 0,
  maxzoom: basemapTileJson.maxzoom ?? 14,
  attribution: basemapTileJson.attribution,
};
await writeFile(
  resolve(vendorDir, 'openfreemap-liberty.json'),
  `${JSON.stringify(basemapStyle)}\n`,
  'utf8',
);
const backgroundLayer = basemapStyle.layers.find((layer) => layer.type === 'background');
await writeFile(
  resolve(vendorDir, 'openfreemap-shell.json'),
  `${JSON.stringify({
    version: 8,
    name: 'Transit-first OpenFreeMap shell',
    sprite: basemapStyle.sprite,
    glyphs: basemapStyle.glyphs,
    sources: {},
    layers: backgroundLayer ? [backgroundLayer] : [],
  })}\n`,
  'utf8',
);
await writeFile(
  resolve(vendorDir, 'README.md'),
  [
    '# Vendored map libraries',
    '',
    'Generated by `npm run build:vendor` from pinned npm packages:',
    '',
    '- MapLibre GL JS 5.13.0 — BSD-3-Clause',
    '- PMTiles 4.4.1 — BSD-3-Clause',
    '- OpenFreeMap Liberty style — served locally with a direct OpenFreeMap tile template',
    '',
    'Do not edit the generated JavaScript or CSS files directly.',
    '',
  ].join('\n'),
  'utf8',
);

console.log('Copied pinned browser assets to vendor/.');
